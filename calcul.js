let calculFonction = {

  addition : function additioner(chiffre) {
    let result = 0;
    for(let i = 1; i <= chiffre ; i++) {
      result += i;
    }
  return result;
  },

  multiplication : function multiplier(chiffre) {
    let result = 1;
    for(let i = 1; i <= chiffre ; i++) {
      result *= 1;
    }
    return result;
  }
};

